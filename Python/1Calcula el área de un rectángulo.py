#carcula el area de un rectangulo. pidele al usuario que ingrese el largo y el ancho y luego muestra el area

# Solicitar al usuario que ingrese el largo y el ancho del rectángulo
largo = float(input("Ingresa el largo del rectángulo: "))
ancho = float(input("Ingresa el ancho del rectángulo: "))

# Calcular el área del rectángulo (área = largo * ancho)
area = largo * ancho

# Mostrar el resultado
print(f"El área del rectángulo es: {area}") 
